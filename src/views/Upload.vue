<template>
  <div>
    <div class="col-8">
      <div>
        <multiselect v-model="selected" :options="options" :multiple="true">
        </multiselect>
      </div>
      <form @submit.prevent="postNewImage" class="mb-5">
        <croppa
          v-model="croppaImage"
          :width="350"
          :height="350"
          placeholder="Choose an image"
          :placeholder-font-size="0"
          :disabled="false"
          :prevent-white-space="true"
          :show-remove-button="true"
        >
        </croppa>
        <div class="form-group">
          <label for="imageUrl">Image URL</label>
          <input
            v-model="newImageUrl"
            type="text"
            class="form-control ml-2"
            placeholder="Enter the image URL"
            id="imageUrl"
          />
        </div>
        <div class="form-group">
          <label for="imageDescription">Description</label>
          <input
            v-model="newImageDescription"
            type="text"
            class="form-control ml-2"
            placeholder="Enter the image description"
            id="imageDescription"
          />
        </div>
        <button type="submit" class="btn btn-primary ml-2">Post image</button>
      </form>
      <food v-for="card in cards" :key="card.id" :info="card" />
    </div>
  </div>
</template>

<script>
// @ is an alias to /src
import Multiselect from "vue-multiselect";
import Croppa from "vue-croppa";
import Food from "@/components/Food.vue";
import { db, storage } from "@/firebase.js";
import store from "@/store";

export default {
  components: { Multiselect },
  name: "Upload",
  data: function () {
    return {
      croppaImage: null,
      cards: [],
      store,
      newImageDescription: false,
      newImageUrl: "",
      selected: null,
      options: ["riÅ¾a", "jaja", "govedina"],
    };
  },
  mounted() {
    this.getPosts();
  },
  methods: {
    getPosts() {
      db.collection("posts")
        .orderBy("postedat", "desc")
        .get()
        .then((query) => {
          this.cards = [];
          query.forEach((doc) => {
            console.log(doc.id);
            console.log(doc.data());

            const data = doc.data();

            this.cards.push({
              id: doc.id,
              url: data.url,
              time: data.postedat,
              description: data.desc,
            });
          });
        });
    },
    async postNewImage() {
      const imageUrl = this.newImageUrl;
      const imageDescription = this.newImageDescription;
      const imageName = "images/" + imageUrl + ".png";

      this.croppaImage.generateBlob((blob) => {
        storage.ref(imageName).put(blob);
        console.log(blob);
      });

      console.log(this.selected);

      try {
        await db.collection("foods").doc(imageUrl).set({
          ingredients: this.selected,
        });
      } catch (e) {
        console.error(e);
      }

      return;
      db.collection("")
        .add({
          url: imageUrl,
          desc: imageDescription,
          user: store.currentUser,
          postedat: Date.now(),
          ingredients: this.selected,
        })
        .then((doc) => {
          console.log("Spremljeno", doc);
          this.newImageDescription = "";
          this.newImageUrl = "";
          this.getPosts();
        })
        .catch((e) => {
          console.error(e);
        });
    },
  },
  components: {
    Food,
  },
};
</script>

